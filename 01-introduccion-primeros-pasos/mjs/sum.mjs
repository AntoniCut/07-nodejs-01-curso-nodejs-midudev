/**
 * --------------------------
 * -----  `sum(a, b)`   -----
 * --------------------------
 * 
 * - Suma dos números
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 * 
 */

export function sum (a, b) {
  return a + b
}


/** 
 * --------------------------
 * -----  `sub(a, b)`   -----
 * -------------------------- 
 * 
 * - Resta dos números
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 * 
 * */

export function sub (a, b) {
  return a - b
}


/**
 * --------------------------
 * -----  `mult(a, b)`  -----
 * --------------------------
 * 
 * - Multiplica dos números
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 * 
 */
export function mult (a, b) {
  return a * b
}